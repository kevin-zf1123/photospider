/**
 * @brief Finalizes one produced node output through the shared metrics policy.
 *
 * @param output Output generated by an operation and stored in node cache.
 * @param inputs Image inputs used to derive debug and spatial metadata.
 * @param enable_timing Whether timing metadata should be attached.
 * @param execution_ms Measured node execution duration in milliseconds.
 * @return Nothing.
 * @throws std::bad_alloc unchanged when metadata or debug storage exhausts
 * memory.
 * @throws Any other exception propagated by ComputeMetricsRecorder.
 * @note This helper keeps the recursive sequential path aligned with the
 * dispatcher metadata policy.
 */
void finalize_output_metadata(NodeOutput& output,
                              const std::vector<const NodeOutput*>& inputs,
                              bool enable_timing, double execution_ms) {
  compute::ComputeMetricsRecorder::finalize_output_metadata(
      output, inputs, enable_timing, execution_ms);
}

/**
 * @brief Stores the latest compute plan and bounded summary history.
 *
 * @param graph Graph whose inspection state receives the plan snapshot.
 * @param compute_plan Plan produced for the current request.
 * @throws std::bad_alloc if summary history storage cannot grow.
 * @note Full ComputePlan data is kept only for the latest request so
 * inspection history does not copy large task graphs repeatedly.
 */
void remember_facade_compute_plan(GraphModel& graph,
                                  const compute::ComputePlan& compute_plan) {
  graph.last_compute_plan = compute_plan;
  graph.last_compute_plan_summary =
      compute::summarize_compute_plan(graph, compute_plan);
  graph.recent_compute_plan_summaries.push_back(
      *graph.last_compute_plan_summary);
  if (graph.recent_compute_plan_summaries.size() > 16) {
    graph.recent_compute_plan_summaries.erase(
        graph.recent_compute_plan_summaries.begin());
  }
}

/**
 * @brief Selects the planning intent for a ComputeService facade request.
 *
 * @param request Service request whose optional intent may preserve legacy HP
 * behavior.
 * @return Requested intent, or GlobalHighPrecision when the legacy path was
 * selected.
 * @throws Nothing directly.
 * @note A missing intent is only an internal compatibility signal; planning
 * still
 * needs an explicit single-domain compute intent.
 */
ComputeIntent planning_intent_for_request(
    const ComputeService::Request& request) {
  return request.intent.value_or(ComputeIntent::GlobalHighPrecision);
}

/**
 * @brief Builds a planning-layer request from a ComputeService request.
 *
 * @param request Service request carrying target node, intent, and dirty ROI.
 * @param use_parallel_executor Whether the caller selected scheduler-backed
 * execution for this service call.
 * @return Narrow compute::ComputeRequest used by task graph planning.
 * @throws Nothing directly.
 * @note Planning uses only semantic target/domain data and the selected
 * execution mode; cache and telemetry options are consumed later by execution.
 */
compute::ComputeRequest make_planning_request(
    const ComputeService::Request& request, bool use_parallel_executor) {
  return compute::ComputeRequest{planning_intent_for_request(request),
                                 request.node_id, use_parallel_executor,
                                 request.dirty_roi};
}

/**
 * @brief Builds a dispatcher request for full HP task execution.
 *
 * @param request Service request whose cache and telemetry options are used by
 * the dispatcher.
 * @return Immutable dispatcher options for one GlobalHighPrecision run.
 * @throws std::bad_alloc if copying the cache precision string allocates.
 * @note Dirty ROI and intent are intentionally ignored here; dirty and RT
 * semantics are coordinated before this full-frame dispatcher path is invoked.
 */
compute::ComputeTaskDispatcher::ComputeDispatchRequest make_dispatch_request(
    const ComputeService::Request& request) {
  return compute::ComputeTaskDispatcher::ComputeDispatchRequest{
      request.node_id,
      request.cache.precision,
      request.cache.force_recache,
      request.telemetry.enable_timing,
      request.cache.disable_disk_cache,
      request.telemetry.benchmark_events};
}

/**
 * @brief Builds a dirty-executor request from a service request.
 *
 * @param request Service request validated by IntentUpdateCoordinator.
 * @param suppress_graph_downsample Whether HP dirty execution should skip
 * direct HP-to-RT graph downsample writes for this request.
 * @param sibling_commit_gate Optional HP commit gate shared with the RT
 * sibling.
 * @param stabilized_parameters Optional immutable connected-parameter snapshot.
 * @param node_synchronization Optional per-node critical sections shared by the
 * concurrent HP and RT siblings of one RealTimeUpdate transaction.
 * @return DirtyUpdateRequest with the requested dirty ROI.
 * @throws std::bad_alloc if copying request cache precision or gate storage
 * exhausts memory.
 * @throws std::bad_optional_access if called before dirty_roi validation.
 * @note Intent-specific executor selection happens in ComputeService; this
 * request only carries the knobs shared by HP and RT dirty executors.
 */
compute::DirtyUpdateRequest make_dirty_update_request(
    const ComputeService::Request& request,
    bool suppress_graph_downsample = false,
    std::shared_ptr<compute::DirtySiblingCommitGate> sibling_commit_gate =
        nullptr,
    std::shared_ptr<const compute::StabilizedDirtyParameters>
        stabilized_parameters = nullptr,
    std::shared_ptr<compute::DirtyNodeSynchronization> node_synchronization =
        nullptr) {
  return compute::DirtyUpdateRequest{request.node_id,
                                     request.cache.precision,
                                     request.cache.force_recache,
                                     request.telemetry.enable_timing,
                                     request.cache.disable_disk_cache,
                                     request.telemetry.benchmark_events,
                                     request.dirty_roi.value(),
                                     suppress_graph_downsample,
                                     std::move(sibling_commit_gate),
                                     std::move(stabilized_parameters),
                                     std::move(node_synchronization)};
}

/**
 * @brief Creates the legacy HP-only request used inside intent callbacks.
 *
 * @param request Intent-aware request being coordinated.
 * @return Copy of request with intent and dirty ROI cleared.
 * @throws std::bad_alloc if copying cache precision allocates.
 * @note IntentUpdateCoordinator calls this through HP fallback callbacks; the
 * cleared intent prevents recursion back into intent coordination.
 */
ComputeService::Request make_global_high_precision_request(
    const ComputeService::Request& request) {
  ComputeService::Request hp_request = request;
  hp_request.intent = std::nullopt;
  hp_request.dirty_roi = std::nullopt;
  return hp_request;
}

/**
 * @brief Creates an HP dirty update request that suppresses benchmark output.
 *
 * @param request Dirty intent request whose semantic and cache options are
 * preserved.
 * @return Request copy with benchmark_events cleared.
 * @throws std::bad_alloc if copying cache precision allocates.
 * @note The old HP side-update callback passed nullptr for benchmark_events;
 * keeping that behavior avoids double-counting benchmark records in RT updates.
 */
ComputeService::Request make_silent_dirty_request(
    const ComputeService::Request& request) {
  ComputeService::Request silent_request = request;
  silent_request.telemetry.benchmark_events = nullptr;
  return silent_request;
}

/**
 * @brief Builds the node/cache-pruned plan used by sequential compute.
 *
 * @param graph Graph that supplies topology, node metadata, and cache state.
 * @param request Intent, target node, and optional dirty ROI for the request.
 * @param execution_order Topological execution order for the target cone.
 * @return Compute plan pruned to request target and cache availability.
 * @throws std::bad_alloc unchanged when graph expansion or plan storage
 * exhausts memory.
 * @throws GraphError from full task graph expansion or pruning.
 * @note Dirty update executors have their own same-boundary helper because
 * they also record dirty snapshots and materialized work sets.
 */
compute::ComputePlan prune_facade_node_cache_task_graph(
    GraphModel& graph, const compute::ComputeRequest& request,
    const std::vector<int>& execution_order) {
  compute::NodeCacheTaskGraphPruner node_cache_pruner;
  const std::shared_ptr<const compute::FullTaskGraph> full_graph =
      compute::get_or_expand_full_task_graph(graph, request.intent);
  return node_cache_pruner.prune(*full_graph, request, execution_order, graph);
}

/**
 * @brief Reports whether one service request belongs to paired realtime work.
 *
 * @param request Request whose optional intent selects the domain.
 * @return true only for explicit RealTimeUpdate.
 * @throws Nothing.
 * @note Missing intent and explicit GlobalHighPrecision are both non-realtime
 * HP requests and receive one issue #66 ComputeRun.
 */
bool is_realtime_request(const ComputeService::Request& request) {
  return request.intent == ComputeIntent::RealTimeUpdate;
}

/**
 * @brief Captures immutable issue #66 HP Run inputs before service planning.
 *
 * @param graph Graph supplying the topology-only submission revision.
 * @param request Service request supplying graph identity, target, and QoS.
 * @return Owned descriptor inputs for one GlobalHighPrecision ComputeRun.
 * @throws std::bad_alloc if graph identity copying allocates.
 * @note topology_generation is an audit snapshot only, not the authoritative
 * GraphRevision or commit predicate introduced by issue #72.
 */
compute::ComputeRunSubmission make_hp_run_submission(
    const GraphModel& graph, const ComputeService::Request& request) {
  return compute::ComputeRunSubmission{
      request.graph_identity,
      compute::ComputeRunSubmissionRevision{graph.topology_generation()},
      request.node_id,
      ComputeIntent::GlobalHighPrecision,
      compute::ComputeRunQuality::Full,
      request.qos};
}

/**
 * @brief Settles one request-owned HP Run around an execution callback.
 *
 * @tparam Execute Callable returning the validated target NodeOutput reference.
 * @param run Run whose admission and exactly-one terminal outcome are owned by
 * this wrapper.
 * @param execute Sequential, full scheduler, or standalone dirty HP callback.
 * @return Mutable target output returned by execute.
 * @throws Rethrows the exact callback exception after publishing Run failure.
 * @throws GraphError if another terminal claimant unexpectedly wins before a
 * normal current-path return.
 * @note A normal return publishes success only after the callback has validated
 * committed or reusable target output. Failure publication retains the
 * original std::exception_ptr before rethrow.
 */
template <typename Execute>
NodeOutput& execute_request_owned_hp_run(compute::ComputeRun& run,
                                         Execute&& execute) {
  try {
    run.advance_to(compute::ComputeRunPhase::Admitted);
    NodeOutput& output = std::forward<Execute>(execute)();
    if (!run.publish_succeeded()) {
      throw GraphError(
          GraphErrc::ComputeError,
          "ComputeRun terminal outcome was claimed before HP success.");
    }
    return output;
  } catch (...) {
    const std::exception_ptr failure = std::current_exception();
    (void)run.publish_failed(failure);
    std::rethrow_exception(failure);
  }
}

